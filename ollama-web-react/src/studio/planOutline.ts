/**
 * Черновой план из запроса пользов (до подключения LLM). Структурированный markdown для согласования.
 */
export function buildPlanOutlineFromUserPrompt(userMessage: string): string {
  const raw = userMessage.replace(/\s+/g, ' ').trim();
  const line =
    raw.length > 200
      ? `${raw.slice(0, 197)}…`
      : raw || '(пустой запрос — уточните цели проекта)';

  return [
    '## План работ (черновик)',
    '',
    '### 1. Цель',
    line,
    '',
    '### 2. Предполагаемые страницы / экраны',
    '- Главная / лендинг с ключевым сообщением и CTA',
    '- При необходимости: раздел «О продукте», контакты или форма заявки',
    '',
    '### 3. Стек',
    '- React + Vite + TypeScript',
    '- Tailwind CSS, адаптивная вёрстка (mobile-first)',
    '',
    '### 4. Критерии готовности',
    '- Успешная production-сборка (`npm run build`)',
    '- Основные брейкпоинты (≈360 / 768 / 1024 px) выглядят аккуратно',
    '',
    '### 5. Риски / уточнения',
    '- Уточнить у вас бренд, цвета и обязательные интеграции (БД, оплата) перед реализацией',
    '',
    '_После вашего согласования будет запущена реализация и сборка (оркестратор агента)._',
  ].join('\n');
}
